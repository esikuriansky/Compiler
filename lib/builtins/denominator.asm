DENOMINATOR:

  PUSH(FP);
  MOV(FP, SP);
  MOV(R0, FPARG(2));

  CMP(IND(R0), T_FRACTION);
  JUMP_EQ(DENOMINATOR_OF_FRAC);
  PUSH(1);
  CALL(MAKE_SOB_INTEGER);
  DROP(1);
  POP(FP);
  RETURN;

DENOMINATOR_OF_FRAC:

  MOV(R0,INDD(R0,3));
  PUSH(R0);
  CALL(MAKE_SOB_INTEGER);
  DROP(1);
  POP(FP);
  RETURN;